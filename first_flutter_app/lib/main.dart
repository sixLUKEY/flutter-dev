class BigCard extends StatelessWidget {
  const BigCard({
    super.key,
    required this.pair,
  });

  final WordPair pair;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);

    final style = theme.textTheme.displayMedium!.copyWith(
      color: theme.colorScheme.onPrimary,
    );

    return Card(
      color: theme.colorScheme.primary,
      child: Padding(
        padding: const EdgeInsets.all(20),
        child: Text(
          pair.asLowerCase,
          style: style,
          semanticsLabel: "${pair.first} ${pair.second}",
        ),
      ),
    );
  }
}


/*

  1 - Every widget defines a `build()` method that gets automatically called every time the widget's 
      circumstances change so that the widget is always up to date.

  2 - `MyHomePage` tracks changes to the app's current state using the `watch` method

  3 - Every `build` method must return a widget or ( more typically ) a nested tree of widgets.
      In this case the top level widget is `Scaffold`.

  4 - `Column` is one of the most basic layout widgets in Flutter. It takes any number of children 
      and puts them in a column from top to bottom.

  5 - Second text widget takes `appState` and accesses the only member of that class, `current` ( which is a WordPair )
      WordPair provides several helpful getters, such as `asPascalCase` or `asSnakeCase`. Here we used `asLowerCase`

  6 - Make use of trailing commas, they serve as a hint for Dart's auto-formatter to put a newline there. 

*/
